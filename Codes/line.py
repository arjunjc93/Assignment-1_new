import numpy as np
import matplotlib.pyplot as plt
import numpy.linalg as LA

plt.axis([-3,6,-3,6])

plt.axis('on')
plt.grid(True)


x1 = np.arange(-12,12)
y1= (3*x1 +6)/4 
x2 = np.arange(-12,12)
y2= 9-3*x2
x3 = np.arange(-12,12)
y3= x3*0
plt.plot(x1,y1,label='3x-4y+6=0',linewidth=2,color='y')
plt.plot(x2,y2,label='3x+y-9=0',linewidth=2,color='g')
plt.plot(x3,y3,label='y=0',linewidth=2,color='b')


A = np.array([[3,-4],
 [3, 1]])
B = np.array([-6,9]) 
P = np.linalg.solve(A, B) 

C = np.array([[3,-4],
 [0, 1]])
D = np.array([-6,0]) 
Q = np.linalg.solve(C, D) 

E = np.array([[3,1],
 [0, 1]])
F = np.array([9,0]) 
R = np.linalg.solve(E, F) 


plt.text(P[0],P[1],'P')
plt.text(Q[0],Q[1],'Q')
plt.text(R[0],R[1],'R')

plt.savefig('line.pdf')
plt.show()